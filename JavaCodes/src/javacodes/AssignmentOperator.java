
package javacodes;

public class AssignmentOperator 
{
    public static void main(String[] args) 
    {
        int x = 10;
        int y = 5;
        
        x += y;
        System.out.println("result = "+x);
        
        x -= y;
        System.out.println("result = "+x);
        
        x *= y;
        System.out.println("result = "+x);
        
        x /= y;
        System.out.println("result = "+x);
    }
}
