    private void dbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dbMouseClicked
        DefaultTableModel model =(DefaultTableModel)table.getModel();
        if(table.getSelectedRow() >=0)
        {
            model.removeRow(table.getSelectedRow());
        }
        else
        {
            JOptionPane.showMessageDialog(null, "You did not seclect any thing","error..!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_dbMouseClicked

    private void ubMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ubMouseClicked
       DefaultTableModel model =(DefaultTableModel)table.getModel();    
        
           int numberOFrow = table.getSelectedRow();
                    
                    String name = ntx.getText();
                    String phn  = btx.getText();
                    String dep  = ptx.getText();
                    String ad   = atx.getText();
            
                    
                    model.setValueAt(name, numberOFrow,0);
                    model.setValueAt(phn, numberOFrow,1);
                    model.setValueAt(dep, numberOFrow,2);
                    model.setValueAt(ad, numberOFrow,3);
    }//GEN-LAST:event_ubMouseClicked

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
         int selectedRow = table.getSelectedRow();
         DefaultTableModel model =(DefaultTableModel)table.getModel();      
         ntx.setText(model.getValueAt(selectedRow,0).toString());
         btx.setText(model.getValueAt(selectedRow,1).toString());
         ptx.setText(model.getValueAt(selectedRow,2).toString());
         atx.setText(model.getValueAt(selectedRow,3).toString());
    }//GEN-LAST:event_tableMouseClicked

